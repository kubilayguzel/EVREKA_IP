async createParentTransactionForTriggeredTask(triggeredTaskTypeId, createdTaskId, deliveryDateStr) {
    if (!triggeredTaskTypeId) {
        console.log('‚ÑπÔ∏è Tetiklenen i≈ü yok, parent transaction kontrol√º yapƒ±lmayacak');
        return;
    }
    
    console.log('üîç Tetiklenen i≈ü i√ßin parent transaction kontrol√º ba≈ülatƒ±ldƒ±. TaskTypeId:', triggeredTaskTypeId);
    
    try {
        // 1. TransactionType'ƒ± direkt triggered task ID'si ile bul (transactionTypes/7 gibi)
        const triggeredTransactionType = this.allTransactionTypes.find(t => t.id === String(triggeredTaskTypeId));
        
        if (!triggeredTransactionType) {
            console.warn('‚ö†Ô∏è TransactionType bulunamadƒ±. TaskTypeId:', triggeredTaskTypeId);
            return;
        }
        
        console.log('üîç Tetiklenen i≈üin transaction type bilgisi:', {
            id: triggeredTransactionType.id,
            name: triggeredTransactionType.name,
            alias: triggeredTransactionType.alias,
            hierarchy: triggeredTransactionType.hierarchy
        });
        
        // 2. Hierarchy parent deƒüilse √ßƒ±k
        if (triggeredTransactionType.hierarchy !== 'parent') {
            console.log('‚ÑπÔ∏è Tetiklenen i≈üin hierarchy deƒüeri parent deƒüil (' + triggeredTransactionType.hierarchy + '), parent transaction olu≈üturulmayacak');
            return;
        }
        

        
        // 4. Parent transaction olu≈ütur
        console.log('‚úÖ Parent transaction olu≈üturuluyor:', {
            type: triggeredTaskTypeId,
            name: triggeredTransactionType.alias || triggeredTransactionType.name
        });
        
        const parentTransactionData = {
            type: String(triggeredTaskTypeId),
            description: `${triggeredTransactionType.alias || triggeredTransactionType.name} i≈ülemi (Otomatik olu≈üturuldu)`,
            transactionHierarchy: 'parent',
            triggeringTaskId: String(createdTaskId),
            deliveryDate: deliveryDateStr || null,
            timestamp: deliveryDateStr ? new Date(deliveryDateStr).toISOString() : new Date().toISOString()
        };
        
        console.log('üì§ Parent transaction verisi:', parentTransactionData);
        
        const addParentResult = await ipRecordsService.addTransactionToRecord(
            this.matchedRecord.id, 
            parentTransactionData
        );
        
        if (addParentResult.success) {
            console.log('‚úÖ Parent transaction ba≈üarƒ±yla olu≈üturuldu! ID:', addParentResult.data?.id || addParentResult.id);
            showNotification('Ana i≈ülem (' + (triggeredTransactionType.alias || triggeredTransactionType.name) + ') otomatik olarak portf√∂ye eklendi!', 'success');
        } else {
            console.error('‚ùå Parent transaction olu≈üturulamadƒ±:', addParentResult.error);
            showNotification('Ana i≈ülem olu≈üturulamadƒ±: ' + addParentResult.error, 'error');
        }
        
    } catch (error) {
        console.error('‚ùå Parent transaction olu≈üturma hatasƒ±:', error);
        showNotification('Parent transaction olu≈üturma hatasƒ±: ' + error.message, 'error');
    }
}


